  <section>
    <div class="section-title"><span class="swatch tomato"></span>Ingredients</div>
    <div class="ingredients">
      <div class="ing-row"><span class="dot" style="background:var(--cream); border:1px solid #dcdcd6"></span> Lasagna sheets <span class="amount">12 sheets</span></div>
      <div class="ing-row"><span class="dot" style="background:var(--tomato)"></span> Tomato sauce <span class="amount">3 cups</span></div>
      <div class="ing-row"><span class="dot" style="background:#8b5e3c"></span> Ground beef <span class="amount">500 g</span></div>
      <div class="ing-row"><span class="dot" style="background:#f7f7f4; border:1px solid #dcdcd6"></span> Ricotta cheese <span class="amount">2 cups</span></div>
      <div class="ing-row"><span class="dot" style="background:var(--gold)"></span> Shredded mozzarella <span class="amount">2 cups</span></div>
      <div class="ing-row"><span class="dot" style="background:#d8c48a"></span> Parmesan cheese <span class="amount">½ cup</span></div>
      <div class="ing-row"><span class="dot" style="background:var(--basil)"></span> Basil or oregano <span class="amount">1 tsp</span></div>
      <div class="ing-row"><span class="dot" style="background:#e8d9c0"></span> Garlic and onion <span class="amount">to taste</span></div>
      <div class="ing-row"><span class="dot" style="background:#c9b896"></span> Salt and pepper <span class="amount">to taste</span></div>
    </div>
  </section>

  <section>
    <div class="section-title"><span class="swatch basil"></span>Instructions</div>
    <ol class="steps">
      <li class="step">
        <div class="step-num"></div>
        <div class="step-body">
          <h3>Prepare the filling</h3>
          <p>Sauté the onion and garlic, then add the ground beef until browned. Add the tomato sauce and seasoning, and simmer for 15 minutes.</p>
        </div>
      </li>
      <li class="step">
        <div class="step-num"></div>
        <div class="step-body">
          <h3>Boil the lasagna sheets</h3>
          <p>Cook the sheets according to package instructions until soft, then drain and lay them aside to prevent sticking.</p>
        </div>
      </li>
      <li class="step">
        <div class="step-num"></div>
        <div class="step-body">
          <h3>Make the cheese layer</h3>
          <p>Mix the ricotta with half of the parmesan and a pinch of salt and pepper.</p>
        </div>
      </li>
      <li class="step">
        <div class="step-num"></div>
        <div class="step-body">
          <h3>Assemble the layers</h3>
          <p>In a baking dish: a layer of sauce, then lasagna sheets, then the ricotta mixture, then mozzarella — repeat until ingredients are used, finishing with mozzarella and parmesan on top.</p>
        </div>
      </li>
      <li class="step">
        <div class="step-num"></div>
        <div class="step-body">
          <h3>Bake</h3>
          <p>Cover the dish with foil and bake at 180°C (350°F) for 25 minutes, then remove the foil and bake for another 15 minutes until the cheese melts and browns.</p>
        </div>
      </li>
      <li class="step">
        <div class="step-num"></div>
        <div class="step-body">
          <h3>Rest and serve</h3>
          <p>Let the lasagna rest for 10 minutes before slicing so the layers set, then garnish with fresh basil and serve hot.</p>
        </div>
      </li>
    </ol>
  </section>